public class swap_two_no 
{
    public static void main(String[] args) {
        System.out.println("swap two no");
        int a=10,b=20,c;
        System.out.println("before swap value in a:"+a);
        System.out.println("before swap value in b:"+b);
        c=a;
        a=b;
        b=c;
        System.out.println("after swap value in a:" + a);
        System.out.println("after swap value in b:" + b);
    }
    
}
